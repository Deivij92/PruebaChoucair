package co.com.choucair.certificacion.pruebachoucair.model;

public class Device {

    private String osComputer;
    private String version;
    private String languaOs;
    private String typeDevice;
    private String model;
    private String osMobile;

    public String getOsComputer() {
        return osComputer;
    }

    public void setOsComputer(String osComputer) {
        this.osComputer = osComputer;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getLanguaOs() {
        return languaOs;
    }

    public void setLanguaOs(String languaOs) {
        this.languaOs = languaOs;
    }

    public String getTypeDevice() {
        return typeDevice;
    }

    public void setTypeDevice(String typeDevice) {
        this.typeDevice = typeDevice;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getOsMobile() {
        return osMobile;
    }

    public void setOsMobile(String osMobile) {
        this.osMobile = osMobile;
    }
}
